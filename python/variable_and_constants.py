# -- ¿QUÉ ES UNA VARIABLE? --

# Una variable es un lugar en memoria (una especie de caja) en
# el que podemos guardar objetos (números, texto, etc). Esta
# variable posee un identificador o nombre con el cual podemos
# llamarla más tarde cuando la necesitemos.



# -- ASIGNACIÓN DE VARIABLES --

# En Python, creamos las variables asignándoles un valor de la siguiente manera:

#     <identificador> = <valor>

# en este caso el signo = se lee como “asignar”



# -- REGLAS EN EL USO DE IDENTIFICADORES DE VARIABLES --

#     * No pueden empezar con un número.
#     * Deben estar en minúsculas
#     * Para separar las palabras usamos el guion bajo: _
#     * Estas reglas son aplicadas al lenguaje Python,
#         en otros lenguajes puede haber otras reglas.



# -- TIPOS DE VARIABLES EN PYTHON --

a = 28 # → int (entero)
b = 1.5 # → float (decimales)
c = "Hello" # → str (string o cadena de texto)
d = True # → boolean (verdadero o falso)
e = None # → NoneType (Sin valor)
f = "5" # → str (5 y “5” no son lo mismo. La primera es un ente y la segunda una cadena de texto)