# -- EL REPL DE PYTHON --


# REPL, de sus ciglas en ingles: Reed-Evaluation-Print-Loop, es un entorde de trabajo
# que se accede mediante la termial con el comando:
    
#     $ python: si estamos en windows.
#     $ python 3: si estamos en MacOS/Linux, esto se debe a que estos OS vienen con la
#         2.0 de python preinstalada por tanto si no se especifica el numero al final
#         la terminal ejecutara la version 2.0 de python por omision lo que podría
#         provocar conflictos de versiones a la hora de ejecutar aplicaciones.

# El REPL es una de las herramientas más poderosas de python por la cantidad de
# funcionalidades que puede  realizar de forma rapida, sencilla y continua.