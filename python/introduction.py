# -- ¿POR QUÉ APRENDER PYTHON? --

# - Python es lenguaje de programación multiparadigma, ya que soporta
#     orientación a objetos, programación imperativa y, en menor medida,
#     programación funcional. Es un lenguaje interpretado, dinámico y
#     multiplataforma.

# - Python es muy elogiado por su elegante sintaxis y su código legible,
#     si estás comenzando tu carrera de programación, Python se adapta a
#     tus necesidades.

# - Python tienen una amplia gama de usos. Desde procesamiento de datos,
#     al aprendizaje de máquina. Por ello, Python es elegido como el lenguaje
#     de programación de muchas empresas y organizaciones.



# -- CAMPOS DE USO DE PYTHON: --

#     * Frontend: Se encarga de llevar el diseño de una aplicación o sitio web
#         a código
#     * IoT: Se encarga de darle la capacidad de conectarse a internet a elementos
#         que pueden estar a nuestro alrededor.
#     * IA: Se encarga de enseñarle a la computadora a resolver un determinado problema
#         sin la necesidad de estar involucrados constantemente.
#     * Backend: Se encarga de crear la lógica con la cual va a funcionar una determinada
#         aplicación y que va a ser almacenada en un servidor.
#     * DevOps: Se encarga de manejar la información almacenada en la nube de una
#         determinada aplicación.
#     * Data Science: Se encarga de tomar la información relevante de un determinado
#         ambiente y poder sacar conclusiones al respecto.
#     * Videojuegos: Se encarga de combinar la programación, el diseño y la música
#         para generar grandes experiencias a los usuarios.
#     * Desarrollo móvil: Se encarga de crear aplicaciones que serán almacenadas en
#         la PlayStore o AppStore, y que podremos hacer uso de ellas desde nuestros
#         smartphones.