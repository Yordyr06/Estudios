# -- EL NÚCLEO DE UN PROGRAMA: LOS ALGORITMOS --

# Dentro de todo lenguaje de programación existe un núcleo llamado algoritmo.
# Un algoritmo es una serie de pasos ordenados para resolver un problema.
# Este es finito, ordenado, y no ambiguo.



# -- DEFINICIONES DE ALGORITMO --

#     * Algoritmo: Conjunto ordenado de operaciones sistemáticas que permite
#         hacer un cálculo y hallar la solución de un tipo de problema.
#     * Algoritmo: Se denomina algoritmo a un grupo finito de operaciones
#         organizadas de manera lógica y ordenada que permite solucionar un
#         determinado problema.
#     * Algoritmo: una serie de instrucciones o reglas establecidas que, por
#         medio de una sucesión de pasos, permiten arribar a un resultado o solución.
#     * Algoritmo: una secuencia de instrucciones que representan un modelo de
#         solución para determinado tipo de problemas. O bien como un conjunto de
#         instrucciones que realizadas en orden conducen a obtener la solución de
#         un problema.



# -- ¿CÓMO SE DISEÑA UNALGORITMO? --

# En programación, un algoritmo establece, de manera genérica e informal, la secuencia
# de pasos o acciones que resuelve un determinado problema y, para representarlo, se utiliza,
# fundamentalmente, dos tipos de notación: pseudocódigo y diagramas de flujo.

#     - Partes de un Algoritmo:
#         Todo algoritmo debe obedecer a la estructura básica de un sistema,
#         es decir: entrada, proceso y salida.



# -- CARACTERISTICAS DE LOS ALGORITMOS --

# Las características fundamentales que debe cumplir todo algoritmo son:

#     * Un algoritmo debe ser preciso e indicar el orden de realización de cada paso.
#     * Un algoritmo debe estar definido. Si se sigue un algoritmo dos veces, se debe
#         obtener el mismo resultado cada vez.
#     * Un algoritmo debe ser finito. el algoritmo se debe terminar en algún momento;
#         o sea, debe tener un número finito de pasos.
#     * Un algoritmo debe ser legible: El texto que lo describe debe ser claro, tal que
#         permita entenderlo y leerlo fácilmente.