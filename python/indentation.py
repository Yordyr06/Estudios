# -- LA IDENTACIÓN EN PYTHON --

# Python es un lenguaje de programación que busca ser simple y
# fácil de entender, para lograr esto decidieron que la identación
# en python fuera una de las bases a tal modo de que si un bloque de
# código no esta correctamente identado pues no va a funcionar.

# Por esta razón la identación es una de las bases del lenguaje
# python sin lugar a dudas.

def example():
    if 3 > 5:
        print("5 es mayor que 3")
    else:
        print("no imprimas nada")

example()

# La identacion son 4 simples espacions a la izquerda de una linea de
# código que en python asume como la identación, en otros lenguajes se
# utilizan llaves como javaScript o Java. Hay que tener en cuenta que si
# la identación no es correcta como en el ejemplo que se muestra esta
# puede romper de forma severa el código.